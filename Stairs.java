import java.util.*;
import java.io.*;
public class Stairs {
    
    private int x, y = 0;
    
    public Stairs(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getx() {
        return this.x;
    }
    
    public int gety() {
        return this.y;
    }
    
}